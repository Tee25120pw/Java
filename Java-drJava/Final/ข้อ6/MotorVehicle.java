class MotorVehicle{
  protected String model;
  protected String make;
  protected int year;
  protected int seats;
  public MotorVehicle(String model,String make,int year,int seats){
    this.make = make;     this.model = model;
    this.year = year;     this.seats = seats;
  }
  public String getModel(){
    return Model;
  }
  public String getMake(){
    return Make;
  }
  public int getYear(){
    return year;
  }
  public int getSeat(){
    return seats;
  }
}
  