public class Triangle{
  private double s1;
  private double s2;
  private double s3;
  public Triangle()
  {
    s1 = 0;
    s2 = 0;
    s3 = 0;
  }
  public Triangle(double x1, double x2, double x3)
  {
    s1 = x1;
    s2 = x2;
    s3 = x3;
  }
  public double getS1()
  {
    return s1;
  }
  public double getS2()
  {
    return(s2);
  }
  public double getS3()
  {
    return(s3);
  }
  public double getArea()
  {
    double a;
    a = (s1*s2)/2;
    return(a);
  }
  public double getPerimeter()
  {
    double b;
    b = s1+s2+s3;
    return(b);
  }
}
  
  
  
  
  
  
  
  