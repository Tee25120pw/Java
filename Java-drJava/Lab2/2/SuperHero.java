public class SuperHero{
  private String heroName;
  
  public SuperHero(){
    this.heroName = "I am Super Hero. I have special power.";
  }
  public SuperHero(String n){
    this.heroName = n;
  }
  public void setHeroName(String n){
    this.heroName = n;
  }
  public String toString(){
    return heroName;
  }
}